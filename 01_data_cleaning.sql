-- CHECK ABNORMAL DATA
select
  (q40 IS NULL) +
  (q50 IS NULL) +
  (q60 IS NULL) +
  (q70 IS NULL) +
  (q80 IS NULL) +
  (q90 IS NULL) +
  (q100 IS NULL) AS missing_count,
  COUNT(*) AS n
FROM gad7_responses
GROUP BY missing_count
ORDER BY missing_count;

SELECT
  SUM(q40 NOT BETWEEN 0 AND 3) AS bad_q40,
  SUM(q50 NOT BETWEEN 0 AND 3) AS bad_q50,
  SUM(q60 NOT BETWEEN 0 AND 3) AS bad_q60,
  SUM(q70 NOT BETWEEN 0 AND 3) AS bad_q70,
  SUM(q80 NOT BETWEEN 0 AND 3) AS bad_q80,
  SUM(q90 NOT BETWEEN 0 AND 3) AS bad_q90,
  SUM(q100 NOT BETWEEN 0 AND 3) AS bad_q100
FROM gad7_responses;

