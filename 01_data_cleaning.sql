SELECT
    COUNT(*) AS n,
    MIN(age) AS min_age,
    MAX(age) AS max_age,
    ROUND(AVG(age),2) AS mean_age,
    ROUND(STDDEV(age),2) AS std_age
FROM gad7_responses
WHERE age IS NOT NULL;

